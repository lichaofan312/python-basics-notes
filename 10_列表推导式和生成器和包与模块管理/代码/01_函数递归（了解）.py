# 函数递归: 在函数内部调用自己

# 需求: 求5的阶乘(使用函数递归)
#
# 5! = 5 * 4!
# 5! = 5 * 4 * 3!
# 5! = 5 * 4 * 3 * 2!
# 5! = 5 * 4 * 3 * 2 * 1

"""
1 找到公式 n!=n*(n-1)
2 找临界值(递归结束的条件)： n=1 fn(1) = 1
"""


def fn(n):
    if n == 1:
        return 1
    return n * fn(n - 1)


print(fn(5))

